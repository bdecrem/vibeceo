def run_animation(anim, loops_override=None):
    frames = anim['frames']
    delay = anim['delay']
    loops = loops_override or anim['loops']
    name = anim['name']

    for _ in range(loops):
        for frame in frames:
            clear()
            print(f"\n  ✧ {name} ✧\n")
            print(frame)
            time.sleep(delay)

